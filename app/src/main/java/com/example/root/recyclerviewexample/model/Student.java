public class Student {
    private String mName;
    private  String mDept;
    private  int mage;

    public String getmName() {
        return mName;
    }

    public void setmName(String mName) {
        this.mName = mName;
    }

    public String getmDept() {
        return mDept;
    }

    public void setmDept(String mDept) {
        this.mDept = mDept;
    }

    public int getMage() {
        return mage;
    }

    public void setMage(int mage) {
        this.mage = mage;
    }
}
